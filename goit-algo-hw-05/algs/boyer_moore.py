# Алгоритм Боєра-Мура — ефективний алгоритм пошуку підрядка, який сканує текст СПРАВА НАЛІВО.
# Основна ідея: якщо символ не співпадає, ми можемо "перестрибнути" через кілька позицій,
# замість перевірки кожного символу по черзі.

# Приклад використання:
# text = "HELLO WORLD"
# pattern = "WORLD"
#
# Покрокова робота:
# Крок 1: Будуємо таблицю зсувів для "WORLD"
#   {'W': 4, 'O': 3, 'R': 2, 'L': 1, 'D': 5}
#
# Крок 2: Починаємо з позиції 0, порівнюємо СПРАВА НАЛІВО
#   Позиція 0-4: "HELLO"
#   text[4]='O', pattern[4]='D' ✗ → зсув на 5 позицій (символ 'O' не в pattern)
#
# Крок 3: Позиція 5, порівнюємо СПРАВА НАЛІВО
#   text[6]='W', pattern[4]='D' ✗ → зсув
#
# Крок 4: Позиція 6, порівнюємо СПРАВА НАЛІВО
#   text[10]='D' == pattern[4]='D' ✓
#   text[9]='L' == pattern[3]='L' ✓
#   text[8]='R' == pattern[2]='R' ✓
#   text[7]='O' == pattern[1]='O' ✓
#   text[6]='W' == pattern[0]='W' ✓
#   ЗНАЙДЕНО на позиції 6!


def build_shift_table(pattern):
    """Створити таблицю зсувів для алгоритму Боєра-Мура."""
    table = {}
    length = len(pattern)
    # Для кожного символу в підрядку встановлюємо зсув рівний довжині підрядка
    for index, char in enumerate(pattern[:-1]):
        table[char] = length - index - 1
    # Якщо символу немає в таблиці, зсув буде дорівнювати довжині підрядка
    table.setdefault(pattern[-1], length)
    return table


def boyer_moore_search(text, pattern):
    # Створюємо таблицю зсувів для патерну (підрядка)
    shift_table = build_shift_table(pattern)
    i = 0  # Ініціалізуємо початковий індекс для основного тексту

    # Проходимо по основному тексту, порівнюючи з підрядком
    while i <= len(text) - len(pattern):
        j = len(pattern) - 1  # Починаємо з кінця підрядка

        # Порівнюємо символи від кінця підрядка до його початку
        while j >= 0 and text[i + j] == pattern[j]:
            j -= 1  # Зсуваємось до початку підрядка

        # Якщо весь підрядок збігається, повертаємо його позицію в тексті
        if j < 0:
            return i  # Підрядок знайдено

        # Зсуваємо індекс i на основі таблиці зсувів
        # Це дозволяє "перестрибувати" над неспівпадаючими частинами тексту
        i += shift_table.get(text[i + len(pattern) - 1], len(pattern))

    # Якщо підрядок не знайдено, повертаємо -1
    return -1
